/* 
Operadores aritméticos
+ Adição / Concatenação
- Subtração
/ Divisão
* e ** Multiplicação e potenciação
% resto da divisão
*/

//+ Adição / Concatenação
const num1 = 5;
const num2 = 10;
console.log(num1 + num2);

//- Subtração
const num3 = 5;
const num4 = 10;
console.log(num3 - num4);

/// Divisão
const num5 = 10;
const num6= 5;
console.log(num5 / num6);

//Potenciação
const num7 = 2;
const num8= 10;
console.log(num7 ** num8);

//Multiplicação
const num9 = 2;
const num10= 10;
console.log(num9 * num10);

//% resto da divisão
const num11 = 10;
const num12= 3;
console.log(num11 % num12);

//Incremento ++
let contador = 1;
contador++; // 2
contador++; // 3
contador++; // 4
contador++; // 5
console.log(contador);

// Decremento --
let contador1 = 10;
contador1--; // 9
contador1--; // 8
contador1--; // 7
contador1--; // 6
contador1--; // 5
console.log(contador1);
